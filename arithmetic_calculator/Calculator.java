package arithmetic_calculator;

public class Calculator {

	
	Calculator(){
		
	}
	
	public long addNumber(int num1,int num2)
	{
		return num1+num2;
	}
	
	public long substractNumber(int num1,int num2) {
		return	num1-num2;
	}
	
	public long multiplyNumber(int num1,int num2) {
		return num1*num2;
	}
	
	public float divideNumber(int num1,int num2) {
		return num1/num2;	
	}
}
